grade = int(input("Enter the number grade you got on your test: "))

if grade >= 91:
    print ("You got an A")
elif grade >= 81:
    print ("You got a B")
elif grade >= 71:
    print ("You got a C")
elif grade >= 61:
    print ("You got a D")
else:
    print ("You got an F")
    
